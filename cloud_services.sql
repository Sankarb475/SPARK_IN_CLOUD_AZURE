Amazon Glacier
---------------------------------------------
Amazon Glacier is an online file storage web service that provides storage for data archiving and backup.

Glacier is part of the Amazon Web Services suite of cloud computing services, and is designed for long-term storage 
of data that is infrequently accessed and for which retrieval latency times of 3 to 5 hours are acceptable



